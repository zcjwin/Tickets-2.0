# 随机生成姓
def getSurname():
    surname = random.choice(last_names)
    return surname

def getfirstName():
    firstName = random.choice(first_names)
    return firstName

# 随机生成手机号
def getTelNo():
    phone_list = []
    phone_head = random.choice([150, 189, 188, 187, 132, 186, 130, 159,
                                134, 135, 182, 183, 139, 137, 138, 151,
                                152, 158, 159, 177, 176, 181, 185, 180,
                                157, 156, 155, 152])
    phone_end = random.randint(0, 99999999)
    phone = '%s%08d' % (phone_head, phone_end)
    if phone not in phone_list:
        phone_list.append(phone)
    return phone

# 随机生成登录名
def getLoginId():
    code = ''
    for i in range(8):
        add = random.choice([random.randrange(10),chr(random.randrange(65,91))])
        code += str(add)
    return code

def getEmail():
    number = ''.join((str(random.randint(3,9)) for i in range(9)))
    return number + '@' + 'qq.com'


# 随机生成n条用户信息写入到本地文件
def main(filename):
    user_list = []
    with codecs.open(filename,'w','utf-8') as f:
        infocount = int(input("请输入您要随机生成的注册信息数量："))
        for i in range(infocount):
            user_dict = {}
            surname = getSurname()
            user_dict['surname'] = surname
            firstName = getfirstName()
            user_dict['firstName'] = firstName
            phoneNum = getTelNo()
            user_dict['contactPhoneNo'] = phoneNum
            email = getEmail()
            user_dict['emailAddress'] = email
            loginId = getLoginId()
            user_dict['loginId'] = loginId
            user_list.append(user_dict)
            f.write(str(user_dict)+'\n')
        # print(user_list)
    print(user_list)
    return user_list


